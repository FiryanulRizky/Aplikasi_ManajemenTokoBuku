    private void supTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_supTableMouseClicked
        
        int brs = supTable.rowAtPoint(evt.getPoint());
        String id = supTable.getValueAt(brs,0).toString();
        idText.setText(id);
        String nama = supTable.getValueAt(brs,1).toString();
        namaText.setText(nama);
        String alamat = supTable.getValueAt(brs,2).toString();
        alamatText.setText(alamat);
        String telp = supTable.getValueAt(brs,3).toString();
        telpText.setText(telp);
        
    }//GEN-LAST:event_supTableMouseClicked

