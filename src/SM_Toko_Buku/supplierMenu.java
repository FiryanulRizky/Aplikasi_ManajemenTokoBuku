    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed

        String id = idText.getText();
        String nama = namaText.getText();
        String alamat = alamatText.getText();
        String telp = telpText.getText();
        
        Connection c = Koneksi.getKoneksi();
        try {
            Statement s = c.createStatement();
            String query = "INSERT INTO `tb_supplier` VALUES ('"+id+"','"+nama+"','"+alamat+"','"+telp+"')";
            s.executeUpdate(query);
            idText.setText("");
            namaText.setText("");
            alamatText.setText("");
            telpText.setText("");
            JOptionPane.showMessageDialog(null, "Supplier Baru Sudah Didaftarkan!");
        } catch (SQLException ex) {
            Logger.getLogger(supplierMenu.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Supplier Gagal Didaftarkan!");
        }
        
        loadData();
        
    }//GEN-LAST:event_addButtonActionPerformed

