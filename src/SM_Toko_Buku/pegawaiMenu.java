    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed

        String id = idText.getText();
        String nama = namaText.getText();
        String user = userText.getText();
        String pass = passText.getText();
        
        Connection c = Koneksi.getKoneksi();
        try {
            Statement s = c.createStatement();
            String query = "INSERT INTO `tb_pegawai` VALUES ('"+id+"','"+user+"','"+pass+"','1','"+nama+"','logout')";
            s.executeUpdate(query);
            idText.setText("");
            namaText.setText("");
            userText.setText("");
            passText.setText("");
            JOptionPane.showMessageDialog(null, "Pegawai Baru Sudah Didaftarkan!");
        } catch (SQLException ex) {
            Logger.getLogger(pegawaiMenu.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Pegawai Gagal Didaftarkan!");
        }
        
        loadData();
        
    }//GEN-LAST:event_addButtonActionPerformed

