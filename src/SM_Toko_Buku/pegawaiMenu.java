    private void pegTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_pegTableMouseClicked
        
        int brs = pegTable.rowAtPoint(evt.getPoint());
        String id = pegTable.getValueAt(brs,0).toString();
        idText.setText(id);
        String nama = pegTable.getValueAt(brs,1).toString();
        namaText.setText(nama);
        String user = pegTable.getValueAt(brs,2).toString();
        userText.setText(user);
        String pass = pegTable.getValueAt(brs,3).toString();
        passText.setText(pass);
        
    }//GEN-LAST:event_pegTableMouseClicked

